function construirFilasResumenMensual(ventas) {
    const resumenPorProducto = {};

    ventas.forEach((venta) => {
        const items = Array.isArray(venta.items) ? venta.items : [];
        items.forEach((item) => {
            const nombre = item.nombre || "Producto sin nombre";
            if (!resumenPorProducto[nombre]) {
                resumenPorProducto[nombre] = {
                    cantidad: 0,
                    total: 0,
                };
            }
            const cantidad = Number(item.cantidad) || 0;
            const precioUnitario = Number(item.precioUnitario) || 0;
            const subtotal = Number(item.subtotal) || (cantidad * precioUnitario);
            resumenPorProducto[nombre].cantidad += cantidad;
            resumenPorProducto[nombre].total += subtotal;
        });
    });

    const filas = Object.entries(resumenPorProducto)
        .sort((a, b) => a[0].localeCompare(b[0], "es"))
        .map(([producto, data]) => {
            const cantidad = data.cantidad || 0;
            const total = data.total || 0;
            const precioPromedio = cantidad > 0 ? Math.round(total / cantidad) : 0;
            return {
                Producto: producto,
                "Cantidad Mensual": cantidad,
                "Precio Unitario Promedio CLP": precioPromedio,
                "Total Producto CLP": total,
            };
        });

    const totalCantidad = filas.reduce((acc, f) => acc + (Number(f["Cantidad Mensual"]) || 0), 0);
    const totalGeneral = filas.reduce((acc, f) => acc + (Number(f["Total Producto CLP"]) || 0), 0);
    const totalVentasMes = ventas.length;

    filas.push({
        Producto: "TOTAL GENERAL",
        "Cantidad Mensual": totalCantidad,
        "Precio Unitario Promedio CLP": "",
        "Total Producto CLP": totalGeneral,
    });
    filas.push({
        Producto: "",
        "Cantidad Mensual": "",
        "Precio Unitario Promedio CLP": "",
        "Total Producto CLP": "",
    });
    filas.push({
        Producto: "TOTAL VENTAS DEL MES",
        "Cantidad Mensual": totalVentasMes,
        "Precio Unitario Promedio CLP": "",
        "Total Producto CLP": "",
    });
    filas.push({
        Producto: "TOTAL UNIDADES VENDIDAS",
        "Cantidad Mensual": totalCantidad,
        "Precio Unitario Promedio CLP": "",
        "Total Producto CLP": "",
    });
    filas.push({
        Producto: "TOTAL RECAUDADO MES",
        "Cantidad Mensual": "",
        "Precio Unitario Promedio CLP": "",
        "Total Producto CLP": totalGeneral,
    });

    return filas;
}

