function formatoFechaCorta(fecha) {
    return new Intl.DateTimeFormat("es-CL", {
        day: "2-digit",
        month: "2-digit",
        year: "numeric",
    }).format(fecha);
}

function formatoHoraCorta(fecha) {
    return new Intl.DateTimeFormat("es-CL", {
        hour: "2-digit",
        minute: "2-digit",
    }).format(fecha);
}

function formatoFechaCompleta(fecha) {
    return new Intl.DateTimeFormat("es-CL", {
        weekday: "long",
        day: "2-digit",
        month: "long",
        year: "numeric",
        hour: "2-digit",
        minute: "2-digit",
        second: "2-digit",
    }).format(fecha);
}

function nombreMes(fecha = new Date()) {
    const mes = new Intl.DateTimeFormat("es-CL", { month: "long" }).format(fecha);
    return mes
        .normalize("NFD")
        .replace(/[\u0300-\u036f]/g, "")
        .toLowerCase();
}

function obtenerVentasPeriodoVisible() {
    const limite = limiteRetencionVentas();
    const hoy = new Date();
    return limpiarVentasAntiguas()
        .filter((venta) => {
            const fecha = new Date(venta.fecha);
            return !Number.isNaN(fecha.getTime()) && fecha >= limite && fecha <= hoy;
        })
        .sort((a, b) => new Date(a.fecha) - new Date(b.fecha));
}

function construirCajaTexto(titulo, lineas, anchoMinimo = 72) {
    const todas = [titulo, ...lineas].map((l) => String(l ?? ""));
    const anchoContenido = Math.max(
        anchoMinimo,
        ...todas.map((l) => l.length)
    );
    const borde = `+${"-".repeat(anchoContenido + 2)}+`;
    const centrar = (txt) => {
        const base = String(txt ?? "");
        const espacioTotal = Math.max(0, anchoContenido - base.length);
        const izq = Math.floor(espacioTotal / 2);
        const der = espacioTotal - izq;
        return `${" ".repeat(izq)}${base}${" ".repeat(der)}`;
    };
    const fila = (txt = "") => `| ${String(txt).padEnd(anchoContenido, " ")} |`;

    return [
        borde,
        fila(centrar(titulo)),
        borde,
        ...lineas.map((l) => fila(l)),
        borde,
    ].join("\n");
}

function construirResumenVentas(ventas) {
    if (ventas.length === 0) {
        return "No hay ventas registradas.";
    }

    const acumuladoPorDia = {};
    ventas.forEach((venta) => {
        const fecha = new Date(venta.fecha);
        const keyDia = fecha.toLocaleDateString("en-CA");
        if (!acumuladoPorDia[keyDia]) {
            acumuladoPorDia[keyDia] = {
                fecha,
                total: 0,
                cantidadVentas: 0,
            };
        }
        acumuladoPorDia[keyDia].total += Number(venta.total) || 0;
        acumuladoPorDia[keyDia].cantidadVentas += 1;
    });

    const lineas = [];
    lineas.push("Resumen de ventas registradas");
    lineas.push(`Generado: ${formatoFechaCompleta(new Date())}`);
    lineas.push("");

    const diasOrdenados = Object.values(acumuladoPorDia).sort((a, b) => a.fecha - b.fecha);
    diasOrdenados.forEach((dia) => {
        lineas.push(`${formatoFechaCorta(dia.fecha)}: ${dia.cantidadVentas} venta(s) - ${formatoCLP(dia.total)}`);
    });

    lineas.push("");
    lineas.push("Detalle de ventas:");
    ventas.forEach((venta) => {
        const fecha = new Date(venta.fecha);
        lineas.push(`- ${formatoFechaCompleta(fecha)} | ${formatoCLP(venta.total)}`);
    });

    lineas.push("");
    const totalGeneral = ventas.reduce((acc, venta) => acc + (Number(venta.total) || 0), 0);
    lineas.push(`TOTAL GENERAL: ${formatoCLP(totalGeneral)}`);

    return construirCajaTexto("RESUMEN DE VENTAS", lineas);
}

