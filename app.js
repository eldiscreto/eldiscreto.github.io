function ordenarProductos(lista) {
    return [...lista].sort((a, b) => {
        const aEsSopaipilla = a.nombre.toLowerCase().includes("sopaipilla");
        const bEsSopaipilla = b.nombre.toLowerCase().includes("sopaipilla");

        if (aEsSopaipilla && !bEsSopaipilla) {
            return -1;
        }
        if (!aEsSopaipilla && bEsSopaipilla) {
            return 1;
        }

        const indexA = ordenClasificaciones.indexOf(a.clasificacion);
        const indexB = ordenClasificaciones.indexOf(b.clasificacion);
        const ordenA = indexA === -1 ? 999 : indexA;
        const ordenB = indexB === -1 ? 999 : indexB;

        if (ordenA !== ordenB) {
            return ordenA - ordenB;
        }

        return a.id - b.id;
    });
}

