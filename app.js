function construirLibroExcelVentas(ventas) {
    const wb = XLSX.utils.book_new();

    const detalleHeaders = [
        "Fecha",
        "ID Venta",
        "Producto",
        "Cantidad",
        "Precio Unitario CLP",
        "Subtotal CLP",
        "Total Venta CLP",
    ];
    const resumenHeaders = [
        "Producto",
        "Cantidad Mensual",
        "Precio Unitario Promedio CLP",
        "Total Producto CLP",
    ];

    const filasDetalle = construirFilasDetalleVentas(ventas);
    const filasResumen = construirFilasResumenMensual(ventas);

    const wsDetalle = XLSX.utils.json_to_sheet(filasDetalle, { header: detalleHeaders });
    const wsResumen = XLSX.utils.json_to_sheet(filasResumen, { header: resumenHeaders });

    ajustarAnchosHoja(wsDetalle, detalleHeaders);
    ajustarAnchosHoja(wsResumen, resumenHeaders);

    XLSX.utils.book_append_sheet(wb, wsDetalle, "Detalle Ventas");
    XLSX.utils.book_append_sheet(wb, wsResumen, "Resumen Mensual");

    return wb;
}

