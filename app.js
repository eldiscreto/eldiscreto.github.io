function enviarCompra(e) {
    if (carrito.length === 0) {
        e.preventDefault();
        Swal.fire({
            title: "¡Tu carrito está vacio!",
            text: "Compra algo para continuar con la compra",
            icon: "error",
            confirmButtonText: "Aceptar",
        });
        return;
    }

    if (mensajeCompra) {
        mensajeCompra.value = construirResumenCompra();
    }

    if (formulario) {
        formulario.method = "POST";
        formulario.action = "https://formsubmit.co/tinkypinky991@gmail.com";
        formulario.target = "formsubmit_iframe";
        const campoSiguiente = formulario.querySelector('input[name="_next"]');
        if (campoSiguiente) {
            campoSiguiente.value = `${window.location.origin}/index.html?compra=ok`;
        }
    }

    registrarVentaLocal();

    const spinner = document.querySelector("#spinner");
    if (spinner) {
        spinner.classList.add("d-flex");
        spinner.classList.remove("d-none");
    }

    carrito = [];
    guardarStorage();
    mostrarAnimacionVentaRealizada();

    // Fallback local: aunque FormSubmit no redirija en local, volvemos a index.
    setTimeout(() => {
        window.location.href = "index.html?compra=ok";
    }, 2400);
}

function completarFormularioCompra() {
    if (!formulario) {
        return;
    }

    if (mensajeCompra) {
        mensajeCompra.value = construirResumenCompra();
    }

    const campoSiguiente = formulario.querySelector('input[name="_next"]');
    if (campoSiguiente && campoSiguiente.value.trim() === "") {
        campoSiguiente.value = `${window.location.origin}/index.html?compra=ok`;
    }
}

function construirResumenCompra() {
    if (carrito.length === 0) {
        return "No hay productos en el carrito.";
    }

    const detalle = carrito
        .map((prod) => `- ${prod.nombre} x${prod.cantidad} = ${formatoCLP(calcularSubtotalProducto(prod))}`)
        .join("\n");
    const total = calcularTotalCarrito();
    const fechaVenta = formatoFechaCompleta(new Date());
    const lineas = [
        `FECHA: ${fechaVenta}`,
        "",
        "DETALLE DE PRODUCTOS",
        detalle,
        "",
        "##############################",
        `### TOTAL VENTA: ${formatoCLP(total)} ###`,
        "##############################",
    ];

    return construirCajaTexto("VENTA CONFIRMADA - EL DISCRETO", lineas, 78);
}
