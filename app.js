async function mostrarAvisoStorage(uso, cuota) {
    if (avisoStorageVisible) {
        return;
    }
    avisoStorageVisible = true;
    const porcentaje = Math.min(100, Math.round((uso / cuota) * 100));
    const mensaje = `Storage casi lleno: se recomienda descargar el resumen de ventas del mes, enviarlo al correo y borrar datos para liberar espacio. Uso actual aproximado: ${porcentaje}%.`;
    if (typeof Swal !== "undefined") {
        const resultado = await Swal.fire({
            title: "Almacenamiento casi lleno",
            text: mensaje,
            icon: "warning",
            confirmButtonText: "Enviar resumen y borrar",
            showCancelButton: true,
            cancelButtonText: "Cancelar",
            allowOutsideClick: false,
        });
        if (resultado.isConfirmed) {
            await enviarResumenVentasYBorrar({ omitirConfirmacion: true });
        }
        avisoStorageVisible = false;
        return;
    }
    const confirmar = confirm(`${mensaje}\n\nPresiona Aceptar para enviar resumen y borrar.`);
    if (confirmar) {
        await enviarResumenVentasYBorrar({ omitirConfirmacion: true });
        avisoStorageVisible = false;
        return;
    }
    avisoStorageVisible = false;
}

function verificarCapacidadStorage(forzar = false) {
    const ahora = Date.now();
    if (!forzar && ahora - ultimoAvisoStorageMs < INTERVALO_AVISO_STORAGE_MS) {
        return;
    }

    obtenerUsoCuotaStorage().then(({ uso, cuota }) => {
        if (!cuota || cuota <= 0) {
            return;
        }
        if (uso / cuota >= UMBRAL_AVISO_STORAGE) {
            ultimoAvisoStorageMs = Date.now();
            mostrarAvisoStorage(uso, cuota);
        }
    });
}

function inicioDelDia(fecha) {
    return new Date(fecha.getFullYear(), fecha.getMonth(), fecha.getDate());
}

function limiteRetencionVentas() {
    const hoy = new Date();
    const inicioMes = new Date(hoy.getFullYear(), hoy.getMonth(), 1);
    const limite = new Date(inicioMes);
    limite.setDate(limite.getDate() - DIAS_GRACIA_MES);
    return inicioDelDia(limite);
}

function obtenerVentasGuardadas() {
    try {
        const ventas = JSON.parse(localStorage.getItem(CLAVE_VENTAS)) || [];
        return Array.isArray(ventas) ? ventas : [];
    } catch (error) {
        return [];
    }
}

function guardarVentas(ventas) {
    try {
        localStorage.setItem(CLAVE_VENTAS, JSON.stringify(ventas));
        verificarCapacidadStorage();
    } catch (error) {
        mostrarAvisoStorage(CUOTA_LOCALSTORAGE_APROX, CUOTA_LOCALSTORAGE_APROX);
    }
}

function limpiarVentasAntiguas() {
    const limite = limiteRetencionVentas();
    const ventas = obtenerVentasGuardadas();
    const filtradas = ventas.filter((venta) => {
        const fecha = new Date(venta.fecha);
        return !Number.isNaN(fecha.getTime()) && fecha >= limite;
    });

    if (filtradas.length !== ventas.length) {
        guardarVentas(filtradas);
    }

    return filtradas;
}

