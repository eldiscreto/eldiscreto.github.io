function precioAEntero(valor) {
    if (typeof valor === "number") {
        return valor;
    }
    const limpio = String(valor || "")
        .replace(/[^\d,.-]/g, "")
        .replace(/\./g, "")
        .replace(",", ".");
    const numero = Number.parseFloat(limpio);
    return Number.isNaN(numero) ? 0 : Math.round(numero);
}

function formatoCLP(valor) {
    return new Intl.NumberFormat("es-CL", {
        style: "currency",
        currency: "CLP",
        minimumFractionDigits: 0,
        maximumFractionDigits: 0,
    }).format(valor);
}

function calcularSubtotalProducto(prod) {
    return precioAEntero(prod.precio) * prod.cantidad;
}

function calcularTotalCarrito() {
    return carrito.reduce((acc, prod) => acc + calcularSubtotalProducto(prod), 0);
}

function normalizarCarrito(lista) {
    return (Array.isArray(lista) ? lista : []).map((prod) => ({
        ...prod,
        cantidad: Number.isFinite(Number(prod.cantidad)) && Number(prod.cantidad) > 0 ?
            Math.floor(Number(prod.cantidad)) :
            1,
    }));
}

function bytesLocalStorageAprox() {
    let total = 0;
    for (let i = 0; i < localStorage.length; i++) {
        const clave = localStorage.key(i) || "";
        const valor = localStorage.getItem(clave) || "";
        total += new Blob([clave]).size + new Blob([valor]).size;
    }
    return total;
}

async function obtenerUsoCuotaStorage() {
    if (navigator.storage && typeof navigator.storage.estimate === "function") {
        try {
            const estimate = await navigator.storage.estimate();
            if (typeof estimate.usage === "number" && typeof estimate.quota === "number" && estimate.quota > 0) {
                return { uso: estimate.usage, cuota: estimate.quota };
            }
        } catch (error) {
            // Fallback a estimacion local.
        }
    }
    return { uso: bytesLocalStorageAprox(), cuota: CUOTA_LOCALSTORAGE_APROX };
}

